import SwiftUI

struct PollDivider: View {
    let color: Color = HMSUIThemeCenter.sharedTheme.colors.borderDefault
    let width: CGFloat = 1
    var body: some View {
        Rectangle()
            .fill(color)
            .frame(height: width)
            .edgesIgnoringSafeArea(.horizontal)
    }
}
